"""Commit + push subprocess boundary between worker success and PR open.

`gh pr create` is not a substitute for `git push`. The orchestrator
needs a small adapter that owns the three git operations between
`run_worker` (when the worker's `.task_complete` sentinel shows up)
and `open_pr`:

- `status_is_dirty(worktree)` — any tracked-file changes?
- `commit_all(worktree, message) -> sha` — stage + commit, refusing
  empty commits; returns the new commit SHA.
- `push_branch(worktree, branch, remote="origin")` — first push with
  upstream-set so subsequent runs of `gh pr create` see the branch.

All git invocations pass `-C <worktree>` so the adapter is robust to
the caller's cwd, matching the pattern the `WorktreeManager` already
uses.

Credentials are the operator's responsibility (ssh-agent or
`gh auth`'s git credential helper). The adapter doesn't manage auth
and doesn't retry on transient push failures.
"""

from __future__ import annotations

import shutil
import subprocess
from pathlib import Path

from turma.errors import PlanningError


GIT_INSTALL_HINT = (
    "git CLI not found. Install it (e.g. `brew install git` on macOS) "
    "and re-run."
)


COMMIT_MESSAGE_TEMPLATE = """\
[{turma_type}] {task_title}

Closes {task_id}.

Generated by turma run for feature "{feature}".\
"""


class GitAdapter:
    """Thin subprocess wrapper around `git -C <worktree> …`."""

    def __init__(self) -> None:
        if shutil.which("git") is None:
            raise PlanningError(GIT_INSTALL_HINT)

    def status_is_dirty(self, worktree: Path) -> bool:
        """True iff `git status --porcelain=v1` emits any tracked changes."""
        result = self._run(
            [
                "git", "-C", str(worktree),
                "status", "--porcelain=v1",
            ],
            step="git status",
        )
        return bool(result.stdout.strip())

    def commit_all(self, worktree: Path, message: str) -> str:
        """Stage everything, commit, and return the new commit SHA.

        Refuses to create an empty commit: if `status_is_dirty` is
        False at the start of the call, raises `PlanningError` with
        a fixed message the orchestrator converts into a worker-
        failure path ("worker reported success but left the tree
        clean").
        """
        if not self.status_is_dirty(worktree):
            raise PlanningError(
                "nothing to commit: worktree is clean. "
                "Did the worker actually modify any tracked files?"
            )

        self._run(
            ["git", "-C", str(worktree), "add", "-A"],
            step="git add",
        )
        self._run(
            ["git", "-C", str(worktree), "commit", "-m", message],
            step="git commit",
        )
        result = self._run(
            ["git", "-C", str(worktree), "rev-parse", "HEAD"],
            step="git rev-parse",
        )
        sha = result.stdout.strip()
        if not sha:
            raise PlanningError(
                "git rev-parse HEAD returned empty stdout"
            )
        return sha

    def push_branch(
        self,
        worktree: Path,
        branch: str,
        *,
        remote: str = "origin",
    ) -> None:
        """Push the task branch to `remote`, setting upstream on first push."""
        self._run(
            [
                "git", "-C", str(worktree),
                "push", "--set-upstream", remote, branch,
            ],
            step="git push",
        )

    def fetch_and_ff_base(
        self, repo_root: Path, base_branch: str
    ) -> None:
        """Fast-forward local `<base_branch>` from origin in one
        subprocess call.

        argv: `git -C <repo_root> fetch origin <base_branch>:<base_branch>`.

        The colon-form refspec fetches `origin/<base_branch>` and
        fast-forwards local `<base_branch>` to match in a single
        operation. The operator's current HEAD / checked-out branch
        is not disturbed, which matters because the orchestrator
        runs from the operator's shell — they may be on a feature
        branch with local edits.

        Without this fetch, a `turma run` claiming a dependent task
        would `git worktree add` from a stale local
        `<base_branch>`, missing the merge of the parent's PR
        that just landed on origin. See
        `openspec/changes/swarm-merge-advancement-stabilization/
        design.md` "`GitAdapter.fetch_and_ff_base`" for why this
        beats a two-call `fetch + merge --ff-only` alternative.

        Failure mapping:
        - Local `<base_branch>` has diverged from origin →
          `PlanningError` naming the branch and pointing at the
          two `git log <a>..<b>` triage commands operators need
          to compare divergence directions. Triggered by either
          `non-fast-forward` or `[rejected]` in stderr.
        - Any other non-zero exit (network, auth, etc.) →
          `PlanningError` preserving stderr verbatim with a
          `git fetch failed:` prefix.
        """
        argv = [
            "git", "-C", str(repo_root),
            "fetch", "origin", f"{base_branch}:{base_branch}",
        ]
        result = subprocess.run(argv, capture_output=True, text=True)
        if result.returncode == 0:
            return

        stderr = result.stderr or ""
        if "non-fast-forward" in stderr or "[rejected]" in stderr:
            raise PlanningError(
                f"local {base_branch} has diverged from "
                f"origin/{base_branch}; refusing to fast-forward. "
                f"Triage with `git log "
                f"{base_branch}..origin/{base_branch}` and "
                f"`git log origin/{base_branch}..{base_branch}` "
                f"to compare directions.\n{stderr.strip()}"
            )

        detail = stderr.strip() or (result.stdout or "").strip() or "unknown error"
        raise PlanningError(
            f"git fetch failed: exit {result.returncode}\n{detail}"
        )

    @staticmethod
    def _run(
        argv: list[str],
        *,
        step: str,
    ) -> subprocess.CompletedProcess[str]:
        result = subprocess.run(
            argv,
            capture_output=True,
            text=True,
        )
        if result.returncode != 0:
            detail = (
                result.stderr.strip()
                or result.stdout.strip()
                or "unknown error"
            )
            raise PlanningError(
                f"{step} failed: exit {result.returncode}\n{detail}"
            )
        return result
