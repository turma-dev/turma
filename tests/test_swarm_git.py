def test_status_is_dirty_true_when_porcelain_output_non_empty(
    tmp_path: Path,
) -> None:
    seen: list[list[str]] = []

    def run(argv, *, step):
        seen.append(argv)
        return _completed(argv, stdout=" M src/turma/foo.py\n")

    adapter = _make_adapter_with_run(run)
    assert adapter.status_is_dirty(tmp_path) is True
    assert seen == [
        ["git", "-C", str(tmp_path), "status", "--porcelain=v1"]
    ]


def test_status_is_dirty_false_when_porcelain_output_empty(
    tmp_path: Path,
) -> None:
    adapter = _make_adapter_with_run(
        lambda argv, *, step: _completed(argv, stdout="")
    )
    assert adapter.status_is_dirty(tmp_path) is False


def test_status_is_dirty_false_when_only_whitespace(
    tmp_path: Path,
) -> None:
    adapter = _make_adapter_with_run(
        lambda argv, *, step: _completed(argv, stdout="\n   \n")
    )
    assert adapter.status_is_dirty(tmp_path) is False


