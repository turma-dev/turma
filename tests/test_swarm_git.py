def test_commit_all_surfaces_commit_subprocess_failure(
    tmp_path: Path,
) -> None:
    """A failing `git commit` (e.g. pre-commit hook rejection) surfaces stderr."""
    def run(argv, *, step):
        if argv[3:5] == ["status", "--porcelain=v1"]:
            return _completed(argv, stdout=" M file\n")
        if argv[3:5] == ["add", "-A"]:
            return _completed(argv)
        if argv[3:5] == ["commit", "-m"]:
            raise PlanningError(
                "git commit failed: exit 1\n"
                "pre-commit: tests failed"
            )
        raise AssertionError(f"unexpected argv: {argv}")

    adapter = _make_adapter_with_run(run)
    with pytest.raises(PlanningError, match="pre-commit: tests failed"):
        adapter.commit_all(tmp_path, "message")


