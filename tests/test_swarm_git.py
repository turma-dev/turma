def test_commit_all_pins_argv_and_returns_sha(tmp_path: Path) -> None:
    seen: list[list[str]] = []

    def run(argv, *, step):
        seen.append(argv)
        if argv[3:5] == ["status", "--porcelain=v1"]:
            return _completed(argv, stdout=" M file.py\n")
        if argv[3:5] == ["add", "-A"]:
            return _completed(argv)
        if argv[3:5] == ["commit", "-m"]:
            return _completed(argv)
        if argv[3:5] == ["rev-parse", "HEAD"]:
            return _completed(argv, stdout="deadbeefcafef00d\n")
        raise AssertionError(f"unexpected argv: {argv}")

    adapter = _make_adapter_with_run(run)
    sha = adapter.commit_all(tmp_path, "commit message body")

    assert sha == "deadbeefcafef00d"
    # Order: status → add → commit → rev-parse.
    assert [a[3:5] for a in seen] == [
        ["status", "--porcelain=v1"],
        ["add", "-A"],
        ["commit", "-m"],
        ["rev-parse", "HEAD"],
    ]
    # commit argv passes the message as the flag's value.
    commit_argv = next(
        a for a in seen if a[3:5] == ["commit", "-m"]
    )
    assert commit_argv == [
        "git", "-C", str(tmp_path),
        "commit", "-m", "commit message body",
    ]


def test_commit_all_refuses_empty_commit(tmp_path: Path) -> None:
    seen: list[list[str]] = []

    def run(argv, *, step):
        seen.append(argv)
        if argv[3:5] == ["status", "--porcelain=v1"]:
            return _completed(argv, stdout="")  # clean tree
        raise AssertionError(
            f"commit_all must not proceed past status on a clean tree: {argv}"
        )

    adapter = _make_adapter_with_run(run)
    with pytest.raises(PlanningError, match="nothing to commit"):
        adapter.commit_all(tmp_path, "message")

    assert [a[3:5] for a in seen] == [["status", "--porcelain=v1"]]


def test_commit_all_raises_when_rev_parse_returns_empty(
    tmp_path: Path,
) -> None:
    def run(argv, *, step):
        if argv[3:5] == ["status", "--porcelain=v1"]:
            return _completed(argv, stdout=" M x\n")
        if argv[3:5] == ["rev-parse", "HEAD"]:
            return _completed(argv, stdout="")
        return _completed(argv)

    adapter = _make_adapter_with_run(run)
    with pytest.raises(PlanningError, match="rev-parse HEAD returned empty"):
        adapter.commit_all(tmp_path, "message")


