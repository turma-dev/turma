@patch("turma.swarm.git.shutil.which", return_value="/usr/bin/git")
@patch("turma.swarm.git.subprocess.run")
def test_run_surfaces_stderr_on_non_zero_exit(
    mock_run: MagicMock, _which: MagicMock, tmp_path: Path
) -> None:
    """An auth failure on push surfaces git's stderr verbatim."""
    mock_run.return_value = subprocess.CompletedProcess(
        args=["git", "push"],
        returncode=128,
        stdout="",
        stderr=(
            "fatal: Authentication failed for "
            "'https://github.com/turma-dev/turma'"
        ),
    )
    adapter = GitAdapter()
    with pytest.raises(PlanningError) as exc:
        adapter.push_branch(tmp_path, "task/oauth/bd-1")
    msg = str(exc.value)
    assert "git push failed" in msg
    assert "Authentication failed" in msg


