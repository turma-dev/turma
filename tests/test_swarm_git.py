def test_commit_template_uses_task_id_without_bd_prefix() -> None:
    # Prior spec drafts had `Closes bd-{task_id}.` which would render
    # `Closes bd-bd-smoke-1.`. The template uses `{task_id}` verbatim
    # and expects the caller to pass the full bd id.
    assert "Closes bd-" not in COMMIT_MESSAGE_TEMPLATE
    assert "Closes {task_id}." in COMMIT_MESSAGE_TEMPLATE


def test_commit_template_renders_as_expected() -> None:
    rendered = COMMIT_MESSAGE_TEMPLATE.format(
        turma_type="impl",
        task_title="Extract planner primitives",
        task_id="bd-smoke-1",
        feature="oauth",
    )
    assert rendered == (
        "[impl] Extract planner primitives\n"
        "\n"
        "Closes bd-smoke-1.\n"
        "\n"
        'Generated by turma run for feature "oauth".'
    )


